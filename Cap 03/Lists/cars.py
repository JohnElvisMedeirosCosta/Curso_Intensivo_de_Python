cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort() #Ordena os valores da lista em ordem alfabética
print(cars)

cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort(reverse=True) #Ordena os valores da lista em ordem decrescente
print(cars)

cars = ['bmw', 'audi', 'toyota', 'subaru']
print("Here is the original list:")
print(cars)

print("\nHere is the sorted list:")
print(sorted(cars))

print("\nHere is the original list again:")
print(cars)

